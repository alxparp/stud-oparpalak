);

CREATE TABLE CARS (
  "ID" NUMBER(10,0) PRIMARY KEY ,
	"MILEAGE_KM" NUMBER(10,0),
	"ENGINE_CAPACITY" NUMBER(10,0)
);

CREATE TABLE MOTORCYCLES (
  "ID" NUMBER(10,0) PRIMARY KEY,
  "ISSUE_YEAR" NUMBER(10,0)
);

CREATE TABLE SALONS (
  "ID" NUMBER(10,0) PRIMARY KEY ,
	"NAME" VARCHAR2(50),
	"TRANSPORT_ID" NUMBER(10,0)
);

CREATE TABLE SENSORS (
  "ID" NUMBER(10,0) PRIMARY KEY,
	"NAME" VARCHAR2(50)
);

CREATE TABLE SENSOR_TRANSPORT (
  "SENSOR_ID" NUMBER(10,0) ,
	"TRANSPORT_ID" NUMBER(10,0),
  CONSTRAINT constraint_name PRIMARY KEY (SENSOR_ID, TRANSPORT_ID)
);


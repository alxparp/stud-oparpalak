CREATE TABLE TRANSPORTS (
  "ID" NUMBER(10,0) PRIMARY KEY ,
  "NAME" VARCHAR2(100),
  "PRICE" NUMBER(10,0),
  "AVAILABILITY" NUMBER(1,0),
  "PRODUCER_ID" NUMBER(10,0)
 );

CREATE TABLE PRODUCERS (
  "ID" NUMBER(10,0) PRIMARY KEY,
	"NAME" VARCHAR2(50)
);

CREATE TABLE CARGO (
